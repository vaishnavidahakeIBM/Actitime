package conditional_statements;

public class If_else_ladder_2 {
	public static void main(String[]args)
	{
		int m= 95;
		if (m<=60)
		{
			System.out.println("bronze medal");
			
		}
		else if(m<=80)
		{
			System.out.println("silver medal");
			
		}
		else
			
		{
			System.out.println("gold medal");
		}
	}

}
