package conditional_statements;

public class if_else_ladder1 {
	public static void main(String[]args)
	{
		int m=60;
		if(m>=40)
		{
			System.out.println("average");
		}
			else if(m>=60)
			{
				System.out.println("good");
			}
			else if(m>=80)
			{
				System.out.println("very good");
			}
			else 
			{
				System.out.println("best");
		}
	}

}
